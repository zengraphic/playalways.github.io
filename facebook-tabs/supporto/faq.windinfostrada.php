		<h2><span class="num">4</span><a href="#">Quali verifiche devo effettuare in caso di frequente "caduta di linea"?</a></h2>
		<!-- faq list text -->
		<div class="text">
			<p>Se la tua linea cade molto di frequente durante le conversazioni telefoniche, prova a fare queste verifiche: stacca dall'impianto telefonico eventuali cordless / POS (pagamenti bancomat) / modem-router ADSL (se utilizzi l'ADSL) / filtri tripolari o RJ ADSL / decoder terrestri o satellitari collegati al telefono; collega solo un telefono corded (a filo, non cordless) alla presa principale (solitamente la presa principale si trova vicino all'ingresso della tua abitazione); prova un altro telefono fisso sicuramente funzionante, avendo cura di collegarlo alla presa telefonica principale.</p>
			<p>Fai delle prove e se il problema non si ripresenta, prova a ricollegare a ritroso gli apparecchi all'impianto per trovare quello che causa la caduta della chiamata.</p>
			<p>Se non ci sono anomalie, controlla, per quanto possibile, che il doppino delle prese e i cavetti esterni che usi per collegare telefoni non siano usurati o deteriorati.</p>

		<h2><span class="num">3</span><a href="#">C'è un fruscio di sottofondo, come posso eliminarlo?</a></h2>
		<!-- faq list text -->
		<div class="text">
			<p>Verifica che i filtri ADSL siano presenti in ciascuna presa telefonica. Se i filtri sono collegati correttamente ma senti ancora fruscio, prova a sostituirli: possono essersi guastati.</p>
			<p>Se non usi l'ADSL, il problema potrebbe essere causato dal cattivo isolamento del doppino o dalla presenza di apparecchi telefonici come FAX, Teleallarmi, Antifurti, POS, Decoder terrestri o satellitari collegati al telefono. Devi controllare, staccandone uno alla volta, che i tuoi telefoni, fax, segreterie, ecc. non siano la causa del fruscio.</p>
		</div>
